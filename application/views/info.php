            <h3 class="title-w3l">Kanker Serviks</h3>
            </h3>
            <p class="mt-4">- behavior eating  </br> perilaku seseorang yang berhubungan dengan pola makannya. Karena kita membahas tentang kesehatan, jadii attributes eating ini bisa membahas intensitas makan seseorang dalam sehari, apa yang ia konsumsi, nilai gizi, proses pengolahan, dsb.

            </p>
            <p class="mt-4">- behavior personal hygiene  </br>perilaku seseorang yang berkaitan dengan penerapan PHBS dalam kehidupannya sehari-hari. Untuk attributes yang ini, poin bahasannya bisa berupa apakah dia telah melakukan PHBS selama ini, apakah dia rutin olahraga, dsb.</p>
            <p class="mt-4">- intention commitment  </br> yang namanya niat, pasti kaitannya sama komitmen. Komitmen itu kan keterikatan seseorang untuk terus melakukan sesuatu, seseorang harus terus mengayuh sepeda biar dia cepet sampai kan. Semakin kuat komitmennya, semakin besar kemungkinan dia sampai di tempat tujuannya. Komitmen berkaitan dengan apakah dia punya komitmen yang kuat untuk mengetahui kanker serviks dan cara mencegahnya (karena ada bagian yang menyebutkan kalo si kanker serviks ini gabisa sembuh, tapi bisa dicegah)
            </p>
            <p class="mt-4">- attitude consistency </br>konsistensi itu dapat berupa tingkat ketaatan dan kemantapan seseorang dalam melakukan upaya preventif dari kanker serviks, kita tau kalo pap smear test harus dilakukan setiap 2 tahun sekali, jadi aspek konsistensi itu dapat dinilai dari apakah si wanita itu taat dengan apa yang harus dilakukan atau tidak.</p>
            <p class="mt-4">- norm significant person </br> norma itu bersifat subjektif, beda-beda tergantung konstruksi sosial yang ada. Seringkali, konstruksi sosial itu dibentuk oleh orang-orang yang punya pengaruh atau significant person (such as youtuber atau influencer). Kaitannya dengan jurnal ini, tingkah laku seseorang itu bisa banget dipengaruhi dengan hadirnya orang-orang yang punya pengaruh di hidup dia. Dalam konteks ini mungkin ada dokter spesialis obgyn yang bisa memberikan menasehati agar wanita rutin check-up daerah intimnya .
            </p>
            <p class="mt-4">- norm fulfillment  </br>fulfillment itu punya korelasi dengan perasaan senang, merasa diapresiasi, dan merasa hidup kita lebih bermakna ketika kita berhasil melakukan suatu hal yang kita anggap benar. Sayangnya, kita hidup di negara yang masih belum bisa sepenuhnya mengapresiasi sesuatu. Masih banyak kaum mendang-mending yang suka menyela dan membandingkan ketika kita bercerita. Nah, pembentukan behaviour seseorang bisa dipercepat kalo dia tinggal di lingkungan yang suportif (ada support system dari orang terdekat, orang yang dia ajak curhat adalah orang yang tepat) terhadap apa yang ia hadapi. </p>
            <p class="mt-4">- perception vulnerability  </br> Seseorang yang memiliki tingkat kerentanan yang tinggi cenderung lebih mudah stres dan berlaku sebaliknya. Kaitannya dengan atribut ini ialah pembentukan tingkah laku wanita mengenai kanker serviks dapat dilihat dari bagaimana coping mechanisms yang dia lakukan, apakah dia bisa berdamai dengan kondisi yang ia alami sehingga ada dorongan untuk konsistensi melakukan suatu hal.
            </p>
            <p class="mt-4">- perception severity  </br>kondisi (tingkat keparahan seseorang) terhadap suatu kasus medis yang muncul dihidupnya (baik yang ia rasakan sendiri ataupun yang ia lihat dari orang lain) itu bisa menjadi faktor pendorong seseorang untuk melakukan dan membentuk tingkah laku  baru di hidupnya.</p>
            <p class="mt-4">- motivation strength </br>berkaitan dengan seberapa kuat (baik secara fisik maupun psikis) seseorang untuk menghadapi kanker serviks. Seseorang yang memiliki diberkati dengan kondisi fisik dan psikis yang kuat tentunya akan memiliki tingkat motivasi yang berbeda dengan mereka yang tidak dibekali dengan kondisi fisik dan psikis yang bagus.</p>
            <p class="mt-4">- motivation willingness  </br> diartikan sebagai kerelaan. Rela loh ya, bukan pasrah. Kalo rela itu berupaya ikhlas dan berdamai dengan apa yang terjadi dengan dia, tapi tetap mengupayakan yang terbaik, kalo pasrah yaa menurutku udah berpangku tangan. aspek kerelaan ini dapat berupa apakah dia rela untuk melakukan suatu hal terkait kanker serviks-nya sendiri, misal dia punya kanker serviks, apakah ia punya motivasi kuat sehingga ia rela untuk melakukan hal-hal yang mungkin belum menjadi kebiasaan namun harus ia lakukan</p>
            <p class="mt-4">- socialSupport emotionality  </br>sebagai social support untuk orang lain yaa sisi emosionalnya, seseorang yang merasa mendapatkan pemenuhan sisi emosionalnya cenderung bisa lebih percaya dan dipengaruhi dibandingkan seseorang yang tidak mendapatkannya.</p>
            <p class="mt-4">- socialSupport appreciation  </br>apresiasi lebih kearah recognition and full understanding of a situation, bagaimana orang lain dapat memahami perspektif kita. Social support dari aspek apresiasi itu dapat digunakan untuk membentuk tingkah laku karena semakin kita merasa bahwa seseorang itu memberikan mencoba mengerti perspektif kita, maka kita akan semakin percaya kepada orang tersebut.</p>
            <p class="mt-4">- socialSupport instrumental  </br> bentuk support yang menggunakan materi untuk memberikan bantuan secara nyata (fisik) dan langsung kepada yang membutuhkan. Contoh instrumental support adalah pemberian bantuan keuangan, barang, ataupun layanan. </p>
            <p class="mt-4">- empowerment knowledge  </br>pemberian izin kepada orang lain untuk melakukan suatu hal. Tapi, sebelum melakukan hal tersebut, ia harus dibekali dengan knowledge yang cukup. Tingkah laku  seseorang dapat terbentuk ketika ia mendapatkan knowledge baru tentang suatu hal. Dalam konteks jurnal ini, seseorang bisa menjadi lebih aware dengan kanker serviks ketika ia tahu banyak informasi mengenai kanker serviks.</p>
            <p class="mt-4">- empowerment abilities   </br>bersifat lebih kearah praktis (practical). Seseorang dapat melakukan suatu kegiatan yang baru dan menjadikannya sebagai tingkah laku ketika ia telah memiliki ilmu dan mampu mengaplikasikan ilmu tersebut. Contoh apabila berhubungan seks selalu memakai alat pengaman</p>
            <p class="mt-4">- empowerment desires  </br>adalah keinginan seseorang untuk menjaga diri dari potensi kanker serviks, kemudian keinginan tersebut ia tuangkan melalui safe sex, pap smear test rutin, atau hal-hal lainnya.</p>

